# defining two variables

a = 10
b = 20

c = a + b  # assign a + b to c

c += a  # equivalent to c = c + a

c -= a  # equivalent to c = c - a

c *= a  # equivalent to c = c * a

c /= a  # equivalent to c = c / a

c %= a  # equivalent to c = c % a

c **= a  # equivalent to c = c ** a

c //= a   # equivalent to c = c // a
