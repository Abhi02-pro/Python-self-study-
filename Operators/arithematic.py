# defining two variables

a = 5
b = 2

print(a + b)  # addition

print(a - b)  # substraction

print(a * b)  # multiplication

print(a / b)  # division (returns quotient)

print(a % b)  # modulus (return remainder)

print(a**b) # exponential (return a to the power of b)

print(a // b)  # floor division
                # returns integer when both a and b are integer
                # returns float when a or b or both are float
